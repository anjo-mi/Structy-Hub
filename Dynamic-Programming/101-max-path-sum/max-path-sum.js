const maxPathSum = (grid,r=0,c=0,memo={}) => {
  const loc = r + ',' + c;
  if (!grid[r]?.[c]) memo[loc] = -Infinity;
  if (r === grid.length - 1 && c === grid[r].length - 1) memo[loc] = grid[r][c];
  if (loc in memo) return memo[loc];
  memo[loc] = Math.max(
    maxPathSum(grid,r+1,c,memo),
    maxPathSum(grid,r,c+1,memo)
  ) + grid[r][c];
  return memo[loc];
};
