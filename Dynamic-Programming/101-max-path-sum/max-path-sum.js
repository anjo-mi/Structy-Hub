const maxPathSum = (grid,r=0,c=0,memo={}) => {
  if (!grid[r]?.[c]) return null;
  const loc = r + ',' + c;
  if (r === grid.length && c === grid[r].length) memo[loc] = grid[r][c];
  if (loc in memo) return memo[loc];
  memo[loc] = Math.max(
    maxPathSum(grid,r+1,c,memo),
    maxPathSum(grid,r,c+1,memo)
  ) + grid[r][c];
  return memo[loc];
};
